


public class JuniorMathFacade {
	

	   public JuniorMathFacade() {
		
	   }

	   static double cubeRoot(double cube){
	     double answer = Math.cbrt(cube);
	    
	    return answer;
	   }
	   
	   static double squareRoot(double root){
		   double answer = Math.sqrt(root);
		   
		return answer;
	   }
	   
	   static double xToThePowerOfy(double x, double y){
		   double answer = Math.pow(x,y);
		 
		return answer;
	   }
	   
	}